def check_float(str)
  !!Float(str) rescue false
end

def get_price(d)
  dollars = d["dollars"]
  cents = d["cents"]
  if cents.length == 3 then cents = cents.slice(1..2) end
  if check_float(cents) and check_float(dollars)
    total = (dollars.to_f*100+cents.to_f)
  end
  total
end

def build_time(d) 
  days = d["daysArray"]
  time = Array.new
  days.each do |day|
    if day
      day_info = Hash.new
      day_info[:time_string] = if d["hours"].length > 0 then d["hours"] else "all day" end
    else 
      day_info = nil
    end
    time.push(day_info)
  end
  sunday = time[0]
  time.shift
  time.push(sunday)
  time
end

def new_deal(venue, d)
  deal = venue.deals.new(
    :title => d["deal"],
    :kind => d["kind"],
    :unit => if d["unit"] == "%" then "percent_off" else "price" end,
    :price => if d["unit"] == "%" then d["dollars"] else get_price(d) end,
    :time => build_time(d)
  )
  deal.save
end

def find_venue(venue_name)
  venue = Venue.where( :name_url => venue_name ).first
  if venue then return venue else return false end
end 

